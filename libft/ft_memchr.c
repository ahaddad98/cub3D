#include "libft.h"

void	*ft_memchr(const void *s, int c, size_t n)
{
	char	*p1;
	size_t	i;

	p1 = (char *)s;
	i = 0;
	while (i < n)
	{
		if (p1[i] == c)
			return (p1 + i);
		i++;
	}
	return (NULL);
}
